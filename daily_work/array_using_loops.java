package daily_work;

public class array_using_loops {

    public static void main(String[] args) {
        int [] x={10,2,3,4,5,6,7,8} ;
        int [] y = new int[x.length];
        for(int i = 0;i<x.length;i++){

            y[i]=x[i];
        }

        System.out.println(y);


    
    
    }
    
}
