package oops.date24_april;

public class oops2 {
    int x;
    int y;


    public oops2(int a1 ,int a2){

        this.x = a1;
        this.y = a2;
    }
    
    ///defining getter 
    public int getX(){
        return this.x;
    }

    public int getY(){
        return this.y;
    }
    //defineing setter 

    public void setX(int a){

        this.x = a;
    }

    public void setY(int b){

        this.y = b;
    }


    
}
